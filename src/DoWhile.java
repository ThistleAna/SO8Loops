public class DoWhile {
    public static void main(String[] args) {
        // intialise the counter
        int i=0;
        // i = 0...1
        do {
            System.out.println("Taking money....");
            // update the counter
            i++; // now i is 1, 2, 3
        } while (i<=3);// always false
    }
}
